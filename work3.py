import math

radius = input("Please enter the radius of the circle: ") #Asks user to enter in a input
print("r =", radius) #Prints user input
r = int(radius) #converting the string input into an integer
area = math.pi * (r * r) #formula for area of a circle
print("Area =", area) #Prints the area of the circle
